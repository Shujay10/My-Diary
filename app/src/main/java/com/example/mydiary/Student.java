package com.example.mydiary;

// Global Variables

public class Student {

    static String name;
    static String regno;
    static String school;
    static String grade;
    static String email;
    static String phoNoP;
    static String phoNoS;
    static String birthday;
    static String ParentsName;


    public static String getName() {
        return name;
    }

    public static void setName(String name) {
        Student.name = name;
    }

    public static String getRegno() {
        return regno;
    }

    public static void setRegno(String regno) {
        Student.regno = regno;
    }

    public static String getSchool() {
        return school;
    }

    public static void setSchool(String school) {
        Student.school = school;
    }

    public static String getGrade() {
        return grade;
    }

    public static void setGrade(String grade) {
        Student.grade = grade;
    }

    public static String getEmail() {
        return email;
    }

    public static void setEmail(String email) {
        Student.email = email;
    }

    public static String getPhoNoP() {
        return phoNoP;
    }

    public static void setPhoNoP(String phoNoP) {
        Student.phoNoP = phoNoP;
    }

    public static String getPhoNoS() {
        return phoNoS;
    }

    public static void setPhoNoS(String phoNoS) {
        Student.phoNoS = phoNoS;
    }

    public static String getBirthday() {
        return birthday;
    }

    public static void setBirthday(String birthday) {
        Student.birthday = birthday;
    }

    public static String getParentsName() {
        return ParentsName;
    }

    public static void setParentsName(String parentsName) {
        ParentsName = parentsName;
    }
}
